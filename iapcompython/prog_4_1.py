# Programa: Lê dois valores e imprime qual é o maior.

a = int(input('Primeiro valor: '))
b = int(input('Segundo valor: '))
if a > b:
    print('O primeiro valor é maior!')
if b > a:
    print('O segundo valor é maior!')

#Na linha 3  (if a > b:) essa expressão será avaliada, se o seu resultado for verdadeiro, a linha 4 será executada. Se for falso, a linha 4 será ignorada. O mesmo acontece para a condição (if b > a:), se o seu resultado for verdadeiro a linha 6 será executada, se for falso será ignorada.
